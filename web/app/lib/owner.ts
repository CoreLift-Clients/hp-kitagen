// 店主紹介ページ（/owner）のデータ。
// 本文（見出し付きセクション）と、顔写真の対応表をここで一元管理する。
// 料理ギャラリーと同じ「フォルダ方式」。口コミシステムのAPIには依存しない。
//
// ─────────────────────────────────────────────────────────────
// 【本文の編集】
//   OWNER_SECTIONS の heading（小見出し）と body（段落配列）を書き換える。
//   1つの見出しに複数段落を持たせたいときは body に要素を足す。
//   OWNER_PULLQUOTE は本文中で特に伝えたい一文を大きく引用表示するための一言。
//
// 【店主の写真が届いたときの反映手順】
//   1. 写真ファイルを web/public/images/owner/ に置く（ファイル名は owner.jpg）
//   2. 下の OWNER_PHOTO を "/images/owner/owner.jpg" に書き換える
//        （初期は "" 空。空の間は写真枠を出さず、文章のみで自然に成立する）
//   3. 保存すれば自動で表示される
//
//   ※ 縦長・横長どちらでも可。ページ側は縦長ポートレート枠（3:4）で
//     object-cover 表示するため、多少の縦横比のズレは破綻しない。
//   ※ ファイル名を変えたい場合は下の OWNER_PHOTO のパスを合わせること。
// ─────────────────────────────────────────────────────────────

