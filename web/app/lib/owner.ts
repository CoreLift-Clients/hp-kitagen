//   ※ 縦長・横長どちらでも可。ページ側は縦長ポートレート枠（3:4）で
//     object-cover 表示するため、多少の縦横比のズレは破綻しない。
//   ※ ファイル名を変えたい場合は下の OWNER_PHOTO のパスを合わせること。
// ─────────────────────────────────────────────────────────────

